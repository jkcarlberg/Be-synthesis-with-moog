     .              (en1-energy_ch(ix-1))*(en1-energy_ch(ix+1))*
     .              yy(k,ix)/( (energy_ch(ix)-energy_ch(ix-1))*
     .              (energy_ch(ix)-energy_ch(ix+1))) +
     .              (en1-energy_ch(ix-1))*(en1-energy_ch(ix))*
     .              yy(k,ix+1)/( (energy_ch(ix+1)-energy_ch(ix-1))*
     .              (energy_ch(ix+1)-energy_ch(ix)))
