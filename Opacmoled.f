               newxs=10**log_ns(13)
            else
               yout=log_ns(ix2)+(t(i)-t_nh(ix2))*(log_ns(ix2-1)-
     .              log_ns(ix2))/(t_nh(ix2-1)-t_nh(ix2))
               newxs=10**yout
            end if
            
c            if (i.eq.42) then
c     write (*,1001) newxs,10**log_ns(ix2),10**log_ns(ix2-1)
c     write (*,1003) dlog10(numdens(9,1,i)), i
c     stop
c     endif
            aNH(i)=newxs*numdens(9,1,i)
         enddo
      endif
               
      return
